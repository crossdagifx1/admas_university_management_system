// Generates db/supabase.sql — the single script you paste into the Supabase
// SQL editor to provision ATMS as a live Postgres backend.
//
// It derives the reference + trainer seed directly from src/data/seed.js so the
// seeded usernames/emails are byte-identical to what the app's login expects.
//
// Per project decision:
//   • Seed scope ....... 42 real trainers + departments/courses/centers/rooms/users only.
//                        All transactional tables are CREATED but left EMPTY (no mock data).
//   • RLS .............. enabled on every table; the anon key is READ-ONLY (SELECT only).
//   • Auth ............. handled in-app (username doubles as password); no Supabase Auth users.
//
// Run:  node db/gen-supabase-sql.mjs   (or: npm run db:supabase)

import { writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import appSeed from '../src/data/seed.js';

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = join(HERE, 'supabase.sql');
// Same content, also written as a Supabase CLI migration so `supabase db push`
// provisions the project. Single source of truth = this generator.
const MIGRATIONS_DIR = join(HERE, '..', 'supabase', 'migrations');
const MIGRATION = join(MIGRATIONS_DIR, '20260618120000_atms_init.sql');

// ---- SQL literal helpers ---------------------------------------------------
const q = (v) => {
  if (v === null || v === undefined || v === '') return 'NULL';
  if (typeof v === 'number') return String(v);
  return `'${String(v).replace(/'/g, "''")}'`;
};
const row = (vals) => `(${vals.map(q).join(', ')})`;

// ---- Every table the app reads (camelCase state key -> snake_case columns) --
const SCHEMA = `-- ============================================================================
-- ADMAS University TVET Management System (ATMS) — Supabase / Postgres schema
-- Generated by db/gen-supabase-sql.mjs — DO NOT edit by hand; re-run the generator.
-- Safe to re-run: drops & recreates the public ATMS tables, then reseeds.
-- ============================================================================

-- Clean slate (children first to respect FKs).
drop table if exists audit_log cascade;
drop table if exists trainer_evaluations cascade;
drop table if exists assessment_results cascade;
drop table if exists exam_approvals cascade;
drop table if exists coc_registrations cascade;
drop table if exists coop_followups cascade;
drop table if exists skill_gap_training cascade;
drop table if exists course_coverage cascade;
drop table if exists attendance cascade;
drop table if exists complaints cascade;
drop table if exists webinars cascade;
drop table if exists schedules cascade;
drop table if exists trainees cascade;
drop table if exists trainers cascade;
drop table if exists users cascade;
drop table if exists courses cascade;
drop table if exists rooms cascade;
drop table if exists centers cascade;
drop table if exists departments cascade;

-- ---- Reference -------------------------------------------------------------
create table departments (
  id   text primary key,
  name text not null,
  code text
);

create table centers (
  id       text primary key,
  name     text not null,
  location text
);

create table rooms (
  id          text primary key,
  room_number text,
  capacity    int,
  center_id   text references centers(id)
);

create table courses (
  id            text primary key,
  unit_code     text,
  unit_title    text not null,
  credit_hours  int default 0,
  department_id text references departments(id),
  total_los     int default 0
);

create table users (
  id            text primary key,
  username      text unique not null,
  full_name     text not null,
  email         text,
  role          text not null check (role in ('Admin','Coordinator','Trainer','Trainee')),
  department_id text references departments(id),
  mobile_number text,
  status        text not null default 'Active',
  created_at    text
);

create table trainers (
  id              text primary key,
  user_id         text references users(id),
  name            text not null,
  staff_no        text,
  department_id   text references departments(id),
  employment_type text check (employment_type in ('FT','PT')),
  present_status  text,
  rating          real,
  photo_url       text
);

-- ---- Transactional (created empty; filled with real data via the app) ------
create table trainees (
  id                       text primary key,
  student_id               text unique,
  first_name               text,
  last_name                text,
  full_name                text,
  sex                      text,
  academic_level           text,
  section_code             text,
  department_id            text references departments(id),
  entry_year               text,
  city                     text,
  phone                    text,
  emergency_contact_name   text,
  emergency_contact_phone1 text,
  status                   text default 'Active',
  photo_url                text
);

create table schedules (
  id             text primary key,
  entry_year     text,
  section_code   text,
  course_id      text references courses(id),
  trainer_id     text references trainers(id),
  room_id        text references rooms(id),
  center_id      text references centers(id),
  schedule_time1 text,
  schedule_time2 text,
  start_date     text,
  end_date       text
);

create table attendance (
  id          text primary key,
  schedule_id text references schedules(id),
  trainer_id  text references trainers(id),
  date        text,
  month       text,
  present     int default 0,
  absent      int default 0,
  late        int default 0,
  total       int default 0,
  photo_url   text,
  notes       text
);

create table course_coverage (
  id             text primary key,
  schedule_id    text references schedules(id),
  trainer_id     text references trainers(id),
  course_id      text references courses(id),
  total_los      int default 0,
  covered_los    int default 0,
  coverage_notes text,
  date_submitted text
);

create table skill_gap_training (
  id                text primary key,
  trainee_name      text,
  skill_area        text,
  learning_outcomes text,
  duration_hours    int default 0,
  trainer_id        text references trainers(id),
  evidence_url      text,
  date              text
);

create table coop_followups (
  id                text primary key,
  trainee_id        text references trainees(id),
  trainee_name      text,
  organization_name text,
  visit_date        text,
  outcome           text,
  notes             text,
  status            text
);

create table coc_registrations (
  id                text primary key,
  trainee_id        text references trainees(id),
  trainee_name      text,
  department_id     text references departments(id),
  level             text,
  phone             text,
  amount_paid       real default 0,
  registration_date text,
  status            text
);

create table exam_approvals (
  id              text primary key,
  course_id       text references courses(id),
  trainer_id      text references trainers(id),
  exam_type       text check (exam_type in ('Theoretical','Practical')),
  status          text check (status in ('Draft','Pending','Approved')),
  submission_date text,
  approval_date   text,
  plan_details    text
);

create table assessment_results (
  id            text primary key,
  title         text not null,
  course_id     text references courses(id),
  level         text,
  section_code  text,
  avg_score     real,
  pass_rate     real,
  date_recorded text,
  status        text default 'Published'
);

create table trainer_evaluations (
  id         text primary key,
  trainer_id text references trainers(id),
  trainee    real,
  peer       real,
  self       real,
  department real,
  period     text
);

create table webinars (
  id           text primary key,
  title        text not null,
  host         text,
  start_time   text,
  duration_min int,
  registrants  int default 0,
  status       text,
  join_url     text
);

create table complaints (
  id        text primary key,
  subject   text not null,
  category  text,
  raised_by text,
  priority  text check (priority in ('Low','Medium','High')),
  status    text,
  date      text
);

create table audit_log (
  id         uuid primary key default gen_random_uuid(),
  entity     text,
  entity_id  text,
  action     text,
  actor      text,
  changes    jsonb,
  created_at timestamptz default now()
);

-- Helpful indexes
create index idx_schedule_trainer on schedules(trainer_id);
create index idx_attendance_sched on attendance(schedule_id);
create index idx_coverage_sched   on course_coverage(schedule_id);
create index idx_trainee_section  on trainees(section_code);
create index idx_audit_entity     on audit_log(entity, entity_id);
`;

// ---- Row-Level Security: anon = READ WRITE ---------------------------------
const ALL_TABLES = [
  'departments', 'centers', 'rooms', 'courses', 'users', 'trainers', 'trainees',
  'schedules', 'attendance', 'course_coverage', 'skill_gap_training', 'coop_followups',
  'coc_registrations', 'exam_approvals', 'assessment_results', 'trainer_evaluations',
  'webinars', 'complaints', 'audit_log',
];
const RLS = `-- ============================================================================
-- Row-Level Security — anon key may perform all CRUD operations.
-- ============================================================================
${ALL_TABLES.map((t) => `alter table ${t} enable row level security;
create policy "anon read ${t}" on ${t} for select to anon, authenticated using (true);
create policy "anon insert ${t}" on ${t} for insert to anon, authenticated with check (true);
create policy "anon update ${t}" on ${t} for update to anon, authenticated using (true) with check (true);
create policy "anon delete ${t}" on ${t} for delete to anon, authenticated using (true);`).join('\n')}

-- Realtime for live-synced tables (optional; ignore errors if already added).
do $$ begin
  alter publication supabase_realtime add table trainers, trainees, schedules, attendance, course_coverage, complaints;
exception when others then null; end $$;
`;

// ---- Seed (reference + 42 trainers + 43 users + transactional data) ---------
const seedSql = () => {
  const out = [];

  out.push('-- ---- Seed: reference data ----');
  out.push(`insert into departments (id, name, code) values\n  ${appSeed.departments.map((d) => row([d.id, d.name, d.code])).join(',\n  ')};`);
  out.push(`insert into centers (id, name, location) values\n  ${appSeed.centers.map((c) => row([c.id, c.name, c.location])).join(',\n  ')};`);
  out.push(`insert into rooms (id, room_number, capacity, center_id) values\n  ${appSeed.rooms.map((r) => row([r.id, r.roomNumber, r.capacity, r.centerId])).join(',\n  ')};`);
  out.push(`insert into courses (id, unit_code, unit_title, credit_hours, department_id, total_los) values\n  ${appSeed.courses.map((c) => row([c.id, c.unitCode, c.unitTitle, c.creditHours, c.departmentId, c.totalLos])).join(',\n  ')};`);

  out.push('\n-- ---- Seed: user accounts (admin + 42 trainers). Username doubles as password in-app. ----');
  out.push(`insert into users (id, username, full_name, email, role, department_id, mobile_number, status, created_at) values\n  ${appSeed.users.map((u) => row([u.id, u.username, u.fullName, u.email, u.role, u.departmentId, u.mobileNumber, u.status, u.createdAt])).join(',\n  ')};`);

  out.push('\n-- ---- Seed: the 42 real trainers ----');
  out.push(`insert into trainers (id, user_id, name, staff_no, department_id, employment_type, present_status, rating) values\n  ${appSeed.trainers.map((t) => row([t.id, t.userId, t.name, t.staffNo, t.departmentId, t.employmentType, t.presentStatus, t.rating])).join(',\n  ')};`);

  if (appSeed.trainees && appSeed.trainees.length > 0) {
    out.push('\n-- ---- Seed: trainees ----');
    out.push(`insert into trainees (id, student_id, first_name, last_name, full_name, sex, academic_level, section_code, department_id, entry_year, city, phone, emergency_contact_name, emergency_contact_phone1, status) values\n  ${appSeed.trainees.map((t) => row([t.id, t.studentId, t.firstName, t.lastName, t.fullName, t.sex, t.academicLevel, t.sectionCode, t.departmentId, t.entryYear, t.city, t.phone, t.emergencyContactName, t.emergencyContactPhone1, t.status])).join(',\n  ')};`);
  }

  if (appSeed.schedules && appSeed.schedules.length > 0) {
    out.push('\n-- ---- Seed: schedules ----');
    out.push(`insert into schedules (id, entry_year, section_code, course_id, trainer_id, room_id, center_id, schedule_time1, schedule_time2, start_date, end_date) values\n  ${appSeed.schedules.map((s) => row([s.id, s.entryYear, s.sectionCode, s.courseId, s.trainerId, s.roomId, s.centerId, s.scheduleTime1, s.scheduleTime2, s.startDate, s.endDate])).join(',\n  ')};`);
  }

  if (appSeed.attendanceRecords && appSeed.attendanceRecords.length > 0) {
    out.push('\n-- ---- Seed: attendance ----');
    out.push(`insert into attendance (id, schedule_id, trainer_id, date, month, present, absent, late, total, photo_url, notes) values\n  ${appSeed.attendanceRecords.map((a) => row([a.id, a.scheduleId, a.trainerId, a.date, a.month || 'June', a.present, a.absent, a.late, a.total, a.photoUrl, a.notes])).join(',\n  ')};`);
  }

  if (appSeed.courseCoverage && appSeed.courseCoverage.length > 0) {
    out.push('\n-- ---- Seed: course coverage ----');
    out.push(`insert into course_coverage (id, schedule_id, trainer_id, course_id, total_los, covered_los, coverage_notes, date_submitted) values\n  ${appSeed.courseCoverage.map((c) => row([c.id, c.scheduleId, c.trainerId, c.courseId, c.totalLos, c.coveredLos, c.coverageNotes, c.dateSubmitted])).join(',\n  ')};`);
  }

  if (appSeed.skillGapTraining && appSeed.skillGapTraining.length > 0) {
    out.push('\n-- ---- Seed: skill gap training ----');
    out.push(`insert into skill_gap_training (id, trainee_name, skill_area, learning_outcomes, duration_hours, trainer_id, evidence_url, date) values\n  ${appSeed.skillGapTraining.map((s) => row([s.id, s.traineeName, s.skillArea, s.learningOutcomes, s.durationHours, s.trainerId, s.evidenceUrl, s.date])).join(',\n  ')};`);
  }

  if (appSeed.coopFollowups && appSeed.coopFollowups.length > 0) {
    out.push('\n-- ---- Seed: coop followups ----');
    out.push(`insert into coop_followups (id, trainee_id, trainee_name, organization_name, visit_date, outcome, notes, status) values\n  ${appSeed.coopFollowups.map((c) => row([c.id, c.traineeId, c.traineeName, c.organizationName, c.visitDate, c.outcome, c.notes, c.status])).join(',\n  ')};`);
  }

  if (appSeed.cocRegistrations && appSeed.cocRegistrations.length > 0) {
    out.push('\n-- ---- Seed: coc registrations ----');
    out.push(`insert into coc_registrations (id, trainee_id, trainee_name, department_id, level, phone, amount_paid, registration_date, status) values\n  ${appSeed.cocRegistrations.map((c) => row([c.id, c.traineeId, c.traineeName, c.departmentId, c.level, c.phone, c.amountPaid, c.registrationDate, c.status])).join(',\n  ')};`);
  }

  if (appSeed.examApprovals && appSeed.examApprovals.length > 0) {
    out.push('\n-- ---- Seed: exam approvals ----');
    out.push(`insert into exam_approvals (id, course_id, trainer_id, exam_type, status, submission_date, approval_date, plan_details) values\n  ${appSeed.examApprovals.map((e) => row([e.id, e.courseId, e.trainerId, e.examType, e.status, e.submissionDate, e.approvalDate, e.planDetails])).join(',\n  ')};`);
  }

  if (appSeed.assessmentResults && appSeed.assessmentResults.length > 0) {
    out.push('\n-- ---- Seed: assessment results ----');
    out.push(`insert into assessment_results (id, title, course_id, level, section_code, avg_score, pass_rate, date_recorded, status) values\n  ${appSeed.assessmentResults.map((r) => row([r.id, r.title, r.courseId, r.level, r.sectionCode, r.avgScore, r.passRate, r.dateRecorded, r.status])).join(',\n  ')};`);
  }

  if (appSeed.trainerEvaluations && appSeed.trainerEvaluations.length > 0) {
    out.push('\n-- ---- Seed: trainer evaluations ----');
    out.push(`insert into trainer_evaluations (id, trainer_id, trainee, peer, self, department, period) values\n  ${appSeed.trainerEvaluations.map((e) => row([e.id, e.trainerId, e.trainee, e.peer, e.self, e.department, e.period])).join(',\n  ')};`);
  }

  if (appSeed.webinars && appSeed.webinars.length > 0) {
    out.push('\n-- ---- Seed: webinars ----');
    out.push(`insert into webinars (id, title, host, start_time, duration_min, registrants, status, join_url) values\n  ${appSeed.webinars.map((w) => row([w.id, w.title, w.host, w.startTime, w.durationMin, w.registrants, w.status, w.joinUrl])).join(',\n  ')};`);
  }

  if (appSeed.complaints && appSeed.complaints.length > 0) {
    out.push('\n-- ---- Seed: complaints ----');
    out.push(`insert into complaints (id, subject, category, raised_by, priority, status, date) values\n  ${appSeed.complaints.map((c) => row([c.id, c.subject, c.category, c.raisedBy, c.priority, c.status, c.date])).join(',\n  ')};`);
  }

  return out.join('\n');
};

const sql = [
  SCHEMA,
  '',
  seedSql(),
  '',
  RLS,
  '',
  `-- Sanity checks (optional): expect departments=3, courses=6, trainers=42, users=43.`,
  `select 'departments' t, count(*) n from departments
union all select 'trainers', count(*) from trainers
union all select 'users', count(*) from users;`,
  '',
].join('\n');

writeFileSync(OUT, sql, 'utf8');
mkdirSync(MIGRATIONS_DIR, { recursive: true });
writeFileSync(MIGRATION, sql, 'utf8');
writeFileSync(join(MIGRATIONS_DIR, '20260620222000_atms_reset_and_seed.sql'), sql, 'utf8');
console.log(`Wrote ${OUT}`);
console.log(`Wrote ${MIGRATION}`);
console.log(`  departments=${appSeed.departments.length} centers=${appSeed.centers.length} rooms=${appSeed.rooms.length} courses=${appSeed.courses.length}`);
console.log(`  users=${appSeed.users.length} trainers=${appSeed.trainers.length}`);
